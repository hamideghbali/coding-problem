// Given two numbers a and b, find their average: (a + b)/2.

// Function to calculate the average of two numbers
function calculateAverage(a, b) {
    const average = (a + b) / 2;
    return average;
  }
  
  // Example usage:
  const a = 10; // You can change this value to any number
  const b = 20; // You can change this value to any number
  const average = calculateAverage(a, b);
  
  console.log(`The average of ${a} and ${b} is ${average}.`);
  