// Given two real numbers, output the larger value of them.

function getLargerValue(a, b) {
    return a > b ? a : b;
}

// Example usage:
console.log(getLargerValue(5.7, 3.2));  // Output: 5.7
console.log(getLargerValue(-2.4, -3.6)); // Output: -2.4
console.log(getLargerValue(0, 0));  // Output: 0
console.log(getLargerValue(7, 7));  // Output: 7
console.log(getLargerValue(-5, 5));  // Output: 5
